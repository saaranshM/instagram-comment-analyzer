def build_lookup_table():
    """Build a flat lookup table mapping alias -> (brand, model) for fuzzy matching."""
    lookup = []
    for brand, brand_data in CAR_DATABASE.items():
        # Brand-level aliases (model=None means brand-only match)
        for alias in brand_data["aliases"]:
            lookup.append((alias, brand, None))

        # Model-level aliases
        for model, aliases in brand_data["models"].items():
            for alias in aliases:
                lookup.append((alias, brand, model))

    return lookup
